    //儲存
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            JSONObject jbossJson = new JSONObject();
            jbossJson.put("JBOSS_HOME", jbossHome.getText());
            jbossJson.put("PORT", jbossPort.getText());
            jbossJson.put("COMPANY_NAME", "");
            String msg = beforeSaveCheck();
            if (msg.isEmpty()) {
                JBossConfigManager jcm = JBossConfigManager.getInstance();
                jcm.updateJBossConfig(new JBossConfig(jbossJson), "add");
                MainPage.loadJBossList();
                this.dispose();
            } else {
                throw new Exception(msg);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "錯誤", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed
    //瀏覽資料夾
    private void browseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseBtnActionPerformed
        File file = null;
        try {
            JBossConfigManager jcm = JBossConfigManager.getInstance();
            FileSystemView fsv = FileSystemView.getFileSystemView();
            JFileChooser fileChooser = new JFileChooser(fsv.getHomeDirectory());// 得到桌面路径
            fileChooser.setDialogTitle("請選擇JBoss路徑");
            fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int returnVal = fileChooser.showOpenDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                file = fileChooser.getSelectedFile();
                if (file != null) {
                    jbossHome.setText(file.getPath());
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(new JFrame(), ex.getMessage(), "錯誤", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_browseBtnActionPerformed
    private String beforeSaveCheck() {
        String msg = "";
        if (jbossHome.getText().isEmpty()) {
            msg = "JBoss路徑不可為空";
        } else if (jbossPort.getText().isEmpty()) {
            msg = "資料庫IP不可為空";
        }
        return msg;
    }
